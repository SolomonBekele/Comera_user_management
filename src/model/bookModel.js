export class Book {
  constructor(id, title, author, isbn, publicationDate, publisher, genre, language){
    this.id = id;
    this.title = title;
    this.author = author;
    this.isbn = isbn;
    this.publicationDate = publicationDate;
    this.publisher = publisher;
    this.genre = genre;
    this.language = language;
  }
}